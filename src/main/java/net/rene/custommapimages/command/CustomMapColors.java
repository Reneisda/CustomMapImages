    public byte bestColor(Color color) {
        double smallestDistance = Double.MAX_VALUE;
        byte bestColor = 0;
        for (Color c : map.keySet()) {
            double dist = colorDistance(c, color);
            if (dist < smallestDistance) {
                smallestDistance = dist;
                bestColor = map.get(c);
            }
        }
        return bestColor;
    }
